			<div class="section-title text-center">
				<h3>PREGUNTAS FRECUENTEMENTE PLANTEADAS EN LOS TALLERES</h3>
			</div>
					<h5 class="pt-4">
						¿Podemos empezar a enseñar el manual cuando lo único que tenemos en el idioma que queremos enseñar es el mismo manual
					unas pocas porciones de la biblia?</h5>
					<p ALIGN="justify">
						Depende. Por lo general, si uno está enseñando a creyentes cuyo deseo principal es leer esas pocas porciones de la Biblia, y si las
						porciones sonf áciles de leer, entonces es bueno empezar a alfabetizarles con una cartilla. Sin embargo, si las porciones son difíciles
						de leer o si la gente no tiene ganas de leerlas, entonces se deberían encontrar materiales adicionales en el idioma antes de comenzar
						un ministerio de alfabetización. Estos materiales necesitan ser revisados para asegurar que son fáciles de leer (Vea el cap. 16 del libro
						"La Guía para el Líder de Alfabetización").<br>

						Si no hay otros materiales, muchas veces es necesario elaborarlos. Pero antes de empezar con la difícil y cara tarea de empezar a elaborar los
						dichos materiales, investigue primeramente si ya hay materiales elaborados por otras organizaciones, las cuales pueden ser usadas o adaptadas.
						Tenga presente que escribir dichos materiales requiere de capacitación, tiempo y dinero.
					</p>

					<h5 class="pt-4">¿A cuántas personas debemos matricular/inscribir en una clase de alfabetización?</h5>
					<p ALIGN="justify">
						Como mencionamos en el curso, lo ideal es "cada uno enseñe a uno...", por lo que cada maesstro capacitado debería tener sólo un alumno,
						sin embargo, hay lugares donde la necesidad de alfabetización es enorme y las clases son necesarias, entonces 10 o 15 alumnos adultos sería
						el cupo límite para nuevos maestros, ya que controlar una clase con alumnos que tienen diferentes niveles de aprendizaje no es fácil.
						Por lo que por regla general, entre menos alumnos tenga, es más fácil de atender el grupo. Sin embargo la clase puede incrementar si es necesario.
					</p>

					<h5 class="pt-4">Si la clase es muy numerosa, ¿podemos dividirla y reclutar a una persona no capacitada para que se encargue de enseñarle a la otra parte?</h5>
					<p ALIGN="justify">
						No, pero si usted tiene más de 15 alumnos, puede pedirle a una persona no capacitada que le ayude, bajo la supervisión suya, luego pídale
						a su asistente que atienda el siguiente taller de alfabetización para maestros que haya en su localidad. Una clase que pase los límites
						razonables de número de alumnos terminaría siendo una gran frustración tanto para los alumnos como para el maestro.
					</p>

					<h5 class="pt-4">¿Qué hacemos con los participantes del taller que no aprenden a enseñar la cartilla?</h5>
					<p ALIGN="justify">
						En obediencia a los buenos principios de la pedagogía adulta, no decimos que el alumno ha reprobado. Tenga a su disposición un formulario
						por escrito para entregar a los alumnos que no hayan logrado éxito en el taller, (véase el apéndice 6 del libro "La Guía para el Líder de Alfabetización");
						el formulario consiste en una carta que declara que el participante tendrá que enseñar bajo la supervisión de un maestro capacitado hasta que pueda
						participar en otro taller.
					</p>

					<h5 class="pt-4">¿Cómo financiamos nuestras clases de alfabetización?</h5>
					<p ALIGN="justify">
						Las iglesias deben incluir la alfabetización en sus presupuestos anuales. Tal como se comenta en el capítulo 12 del libro "La Guía para el Líder de Alfabetización".
						Se espera que la iglesia compre los materiales requeridos por los maestros, mientras los alumnos deben comprar sus cartillas que conmúnmente no son caras.
					</p>

					<h5 class="pt-4">¿Reciben los maestros de alfabetización alguna ayuda económica por su enseñanza?</h5>
					<p ALIGN="justify">
						Hay dos formas de ver y responder esta pregunta, ya que algunas iglesias y organizaciones sienten que es bueno y necesario pagarles a sus maestros
						de alfabetización; otros prefieren un programa de alfabetización que sea de puros voluntarios.<br>

						Muchos sienten que cuando se vuelve una práctica pagarles a los maestros de la escuela dominical y a los líderes del coro, entonces la iglesia debería
						también pagarle a los maestros de alfabetización al dar las clases. Por ejemplo, hemos conocido iglesias que procuran darles un obsequio a los maestros
						de alfabetización al final de cada año, lo cual puede ser algo muy simbólico como una bolsa de arroz u otro producto producido en la granja, pero la
						mayoría de las iglesias tal vez pueden dar y ofrecer lo que probablemente es el mejor regalo; un reconocimiento de apreciación lo cual se hace
						regularmente.
					</p>

					<h5 class="pt-4">¿Qué se recomienda hacer con las clases programadas durante las épocas de siembra y cosecha?</h5>
					<p ALIGN="justify">
						Lo más recomendable es que los alumnos tomen un receso durante ésta época del año. Pídale a sus alumnos que se reúnan a tomar las clases, con
						tiempo extra durante la época que ellos tengan menos ocupaciones. De esta manera usted puede dejar de dar clases en las épocas más ocupadas
						para ellos.
					</p>

					<h5 class="pt-4">¿Cómo podemos asegurarnos que obtendremos los manuales y otros materiales de alfabetización cuando los necesitemos?</h5>
					<p ALIGN="justify">
						Por lo general este es un problema bastante común. El líder o coordinador nacional o regional debe tener los materiales de alfabetización
						en existencia o llevarlos consigo al viajar por el país o las comunidades. También se sugiere que haya una persona encargada de tener materiales
						en existencia, en las diferentes comunidades o distritos cuando sea posible. Dicha persona será responsable de distribuir los manuales y los libros,
						así como también el importe de ellos, lo cual lo enviará al coordinador. Al tener un encargado en cada área o comunidad con material existente,
						podrá determinar y facilitar el éxito y el crecimiento del programa de alfabetización.
					</p>

					<h5 class="pt-4">¿Cuánto tiempo se requiere para enseñar a leer a un adulto?</h5>
					<p ALIGN="justify">
						El factor más importnte que determinará el tiempo que le tomará a cada persona aprender a leer será la frecuencia con que tome la clase.<br>
						Una cartilla estándar de AEI contiene 72 lecciones. Sin embargo, la cartilla en español contiene 66 leccioens, así que tenga presente la siguiente
						tabla; cuando las personas le pregunten cuánto tiempo la habrá terminado.<br><br>

									Frecuencia de la clases					|					Tiempo para terminar la cartilla<br>
									1 x una semana								= 					66 semanas (16.5 meses)<br>
									2 x una semana								= 					33 semanas (8 meses)<br>
									3 x una semana								= 					22 semanas (5.5 meses)<br>
									4 x una semana								= 					16 semanas (4 meses)<br>
									5 x una semana								= 					13 semanas (3.5 meses)<br>
									6 x una semana								= 					11 semanas (< 3 meses)<br>

									<br>
						Aunque debe recordar, que aun después de que los alumnos hayan terminado la cartilla, ellos necesitarán continuar leyendo otro tipo de materiales,
						tales como periódicos, libros, manuales, la biblia, etc. Aprendiendo a "cómo leer" es un proceso continuo y no debe de terminar con la cartilla. <br>

						Además de la frecuencia de las clases, el nivel de alfabetización y la motivación que los alumnos tengan también son factores importantes que deben
						ser considerados.<br>

						Los adultos que han vivido rodeados de personas que saben leer, peude que aprendan más rápido. Otros que ya han aprendido palabras como "alto",
						"cruce de la vía del tren", "salida" o algunos números, cuando ellos los vean, tendrán una ventaja. Habrá alumnos que se verán beneficiados
						inmediatamente en su vida social y económica al aprender a leer, si es que tuvieran un deseo muy grande de aprender a leer la Biblia, aprenderán
						a leer aún más rápido.
					</p>

					<h5 class="pt-4">¿Cuándo deben los recién alfabetizados asistir a un taller de capacitación?</h5>
					<p ALIGN="justify">
						Lo ideal sería que el maestro de alfabetización le permitiera al nuevo lector ser su asistente en la enseñanza del manual por un periodo de tiempo.
						Lo cual les da confianza. Luego, ellos mismos podrán asistir a un taller de alfabetización para maestros.<br>

						Aunque los nuevos lectores adultos serán sin duda alguna los mejores maestros de alfabetización que los maestros "tradicionales", tal vez no se
						sentirán muy ien en un taller donde los participantes tengan estudios superiores. Sin embargo, no podemos ofrecer talleres especiales para ellos.
						Así que los nuevos lectores tendrán que ser capacitados, junto con los otros participantes.
					</p>
